# Short Hand If Else Notation In Python

'''
a = int(input("Enter a:"))
b = int(input("Enter b:"))

if a>b:
    print("a is greater than b")
else:
    print("b is greater than a")
'''

# this above code can also be written as/in shorthand form:

a = int(input("Enter a:")); b = int(input("Enter b:")); print("a is greater than b") if a>b else print("b is greater than a")

# code becomes short but the code loses its readability